        print(f"   -> ‡πÄ‡∏à‡∏≠‡πÇ‡∏°‡πÄ‡∏î‡∏•‡∏™‡∏≥‡∏£‡∏≠‡∏á: {len(discovered_models)} ‡∏ï‡∏±‡∏ß")
